class relational{
    public static void main(String [] args)
    {
        int a=20 , b=30 , c=20;
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("c="+c);
        System.out.println("a>b="+(a>b));
        System.out.println("c>a="+(c>a));
        System.out.println("a<b="+(a<b));
        System.out.println("c<a="+(c<a));
        System.out.println("a>=b="+(a>=b));
        System.out.println("c>=a="+(c>=a));
        System.out.println("a<=b="+(a<=b));
        System.out.println("c<=a="+(c<=a));
        System.out.println("a=b="+(a=b));
        System.out.println("c=a="+(c=a));
        System.out.println("a!=b="+(a!=b));
        System.out.println("c!=a="+(c!=a));
    }
}